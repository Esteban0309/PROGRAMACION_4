void main() {
  print("Operaciones aritméticas - Sistema de Admisión");
  final double nota1 = 8.5;
  final double nota2 = 7.0;
  print("Suma de notas: $nota1 + $nota2 = ${nota1 + nota2}");
  print("Resta de notas: $nota1 - $nota2 = ${nota1 - nota2}");
  print("Multiplicación (ponderación): $nota1 * $nota2 = ${nota1 * nota2}");
  print("División (promedio): $nota1 / $nota2 = ${nota1 / nota2}");
}
